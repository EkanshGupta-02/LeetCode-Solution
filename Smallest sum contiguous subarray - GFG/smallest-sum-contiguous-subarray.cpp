class Solution{
  public:
  int smallestSumSubarray(vector<int>& a){
      int mn = INT_MAX,currmn = 0;
        for(auto i:a){
            currmn = min(i,currmn+i);
            mn = min(mn,currmn);
        }
        return mn;
  }
};


